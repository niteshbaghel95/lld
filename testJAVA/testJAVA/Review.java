package testJAVA;

public class Review {

    int orderId;
    String comment;
    int rating;
    public int getOrderId() {
        return orderId;
    }
    public void setOrderId(int orderId) {
        this.orderId = orderId;
    }
    public String getComment() {
        return comment;
    }
    public void setComment(String review) {
        this.comment = review;
    }
    public int getRating() {
        return rating;
    }
    public void setRating(int rating) {
        this.rating = rating;
    }
}
